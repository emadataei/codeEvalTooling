const { getPRNumber } = require('./pr-comment-utils');

async function cleanupOldVisualComments({ github, context }) {
  const prNumber = getPRNumber(context);
  if (!prNumber) {
    console.log('No PR number found, skipping cleanup');
    return;
  }

  try {
    // Get all comments on the PR
    const { data: comments } = await github.rest.issues.listComments({
      owner: context.repo.owner,
      repo: context.repo.repo,
      issue_number: prNumber,
    });

    console.log(`Found ${comments.length} existing comments to check`);

    // Identifiers for new separate visual comments
    const separateVisualIdentifiers = [
      'pr-visual-pr-impact-heatmap',
      'pr-visual-development-flow', 
      'pr-visual-story-arc'
    ];

    // Find old combined visual comments and duplicate separate comments to remove
    const oldCommentPatterns = [
      'Enhanced PR Visuals',
      'Real-time analytics with embedded images',
      'PR Impact Heatmap.*Development Flow.*PR Summary', // Old combined format
    ];

    const commentsToDelete = [];

    for (const comment of comments) {
      // Check for old combined visual comments
      const isOldVisualComment = oldCommentPatterns.some(pattern => 
        new RegExp(pattern, 'i').test(comment.body)
      );

      // Check for duplicate separate visual comments (keep only the newest)
      const isVisualComment = comment.body.includes('*Generated by PR Visuals workflow*') ||
                             comment.body.includes('## PR Impact Heatmap') ||
                             comment.body.includes('## Development Flow') ||
                             comment.body.includes('## Story Arc');

      if (isOldVisualComment && comment.body.length > 30000) {
        console.log(`Marking old combined visual comment for deletion (${comment.body.length} chars)`);
        commentsToDelete.push(comment);
      } else if (isVisualComment) {
        // For separate visual comments, check if there are duplicates
        const commentType = comment.body.includes('## PR Impact Heatmap') ? 'heatmap' :
                          comment.body.includes('## Development Flow') ? 'flow' :
                          comment.body.includes('## Story Arc') ? 'arc' : 'unknown';
        
        // Find other comments of the same type
        const sameTypeComments = comments.filter(c => 
          c.id !== comment.id && 
          ((commentType === 'heatmap' && c.body.includes('## PR Impact Heatmap')) ||
           (commentType === 'flow' && c.body.includes('## Development Flow')) ||
           (commentType === 'arc' && c.body.includes('## Story Arc')))
        );

        // If this is an older duplicate (not the most recent), mark for deletion
        if (sameTypeComments.length > 0) {
          const newestSameType = sameTypeComments.reduce((newest, current) => 
            new Date(current.updated_at) > new Date(newest.updated_at) ? current : newest
          );
          
          if (new Date(comment.updated_at) < new Date(newestSameType.updated_at)) {
            console.log(`Marking duplicate ${commentType} visual comment for deletion`);
            commentsToDelete.push(comment);
          }
        }
      }
    }

    // Delete marked comments
    for (const comment of commentsToDelete) {
      try {
        console.log(`Deleting comment ${comment.id} (${comment.body.substring(0, 50)}...)`);
        await github.rest.issues.deleteComment({
          owner: context.repo.owner,
          repo: context.repo.repo,
          comment_id: comment.id,
        });
        
        // Small delay to avoid rate limiting
        await new Promise(resolve => setTimeout(resolve, 300));
      } catch (deleteError) {
        console.error(`Failed to delete comment ${comment.id}:`, deleteError.message);
      }
    }

    console.log(`✅ Cleanup completed - deleted ${commentsToDelete.length} old/duplicate comments`);
  } catch (error) {
    console.error('Failed to cleanup old comments:', error.message);
  }
}

module.exports = cleanupOldVisualComments;
