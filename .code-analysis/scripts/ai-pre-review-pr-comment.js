const { getPRNumber, loadResults, createOrUpdateComment } = require('./pr-comment-utils');

module.exports = async ({ github, context }) => {
  const prNumber = getPRNumber(context);
  
  if (!prNumber) {
    console.log('No PR number found, skipping AI pre-review comment');
    return;
  }
  
  // Load AI pre-review results
  const results = loadResults('ai-pre-review-results.json', { 
    summary: 'AI pre-review analysis failed',
    risk_level: 'UNKNOWN',
    risk_factors: [],
    file_categories: {},
    file_count: 0,
    ai_analysis: {
      summary: 'Analysis failed',
      business_impact: 'Unknown',
      technical_changes: 'Unknown',
      potential_issues: 'Manual review required'
    }
  });
  
  // Build the comment
  const timestamp = new Date().toISOString();
  const runId = process.env.GITHUB_RUN_ID || 'unknown';
  let comment = `## AI Pre-Review Analysis\n\n`;
  comment += `*Generated: ${timestamp} (Run: ${runId})*\n\n`;
  
  // Risk level indicator
  comment += `**Risk Level:** ${results.risk_level}\n\n`;
  
  // Plain-English Summary
  comment += `### What This Change Does\n\n`;
  comment += `${results.ai_analysis.summary}\n\n`;
  
  // Business Impact
  if (results.ai_analysis.business_impact && results.ai_analysis.business_impact !== 'Unknown') {
    comment += `### Business Impact\n\n`;
    comment += `${results.ai_analysis.business_impact}\n\n`;
  }
  
  // Technical Changes
  if (results.ai_analysis.technical_changes && results.ai_analysis.technical_changes !== 'Unknown') {
    comment += `### Technical Changes\n\n`;
    comment += `${results.ai_analysis.technical_changes}\n\n`;
  }
  
  // Risk Assessment
  if (results.ai_analysis.potential_issues && results.ai_analysis.potential_issues !== 'Unknown') {
    comment += `### Things to Watch For\n\n`;
    comment += `${results.ai_analysis.potential_issues}\n\n`;
  }
  
  // File Analysis Summary
  comment += `### Change Summary\n\n`;
  comment += `**Files Changed:** ${results.file_count}\n\n`;
  
  // File categories breakdown
  const categories = results.file_categories;
  const categoryLabels = {
    'ui': 'UI',
    'api': 'API',
    'database': 'Database',
    'config': 'Config',
    'test': 'Test',
    'documentation': 'Documentation',
    'other': 'Other'
  };
  
  const nonEmptyCategories = Object.entries(categories)
    .filter(([_, files]) => files && files.length > 0)
    .map(([category, files]) => `**${categoryLabels[category] || 'Other'}**: ${files.length} files`);
  
  if (nonEmptyCategories.length > 0) {
    comment += `**File Types:**\n${nonEmptyCategories.join('\\n')}\n\n`;
  }
  
  // Risk factors
  if (results.risk_factors && results.risk_factors.length > 0) {
    comment += `### Risk Factors Detected\n\n`;
    results.risk_factors.forEach(factor => {
      comment += `- ${factor}\n`;
    });
    comment += `\n`;
  }
  
  // Footer
  comment += `\n---\n`;
  comment += `*This analysis was generated by AI to help with code review triage. Please use your judgment and conduct thorough manual review as appropriate.*`;
  
  // Create or update the comment
  try {
    await createOrUpdateComment(
      github, 
      context, 
      prNumber, 
      comment, 
      'AI Pre-Review Analysis',  // identifier to find existing comments
      'AI_PRE_REVIEW_COMMENT'   // unique comment ID for reliable matching
    );
  } catch (error) {
    console.error('Error creating or updating AI pre-review comment:', error);
    throw error;
  }
};
